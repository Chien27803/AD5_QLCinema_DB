package com.example.cinemaprofile;

public class User {
    String phone, name, email, birthdate, gender, city, district, address;

    public User(String phone, String name, String email, String birthdate, String gender, String city, String district, String address) {
        this.phone = phone;
        this.name = name;
        this.email = email;
        this.birthdate = birthdate;
        this.gender = gender;
        this.city = city;
        this.district = district;
        this.address = address;
    }
}
